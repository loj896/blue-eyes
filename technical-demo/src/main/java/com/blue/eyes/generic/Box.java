public class Box<T> {
    private T t;

    public T getT() {
        return t;
    }

    public void setT(T t) {
        this.t = t;
    }

    public static void main(String[] args) {
        Box<Integer> intBox = new Box<Integer>();
        intBox.setT(new Integer(10));

        Box<String> strBox = new Box<String>();
        strBox.setT("str");

        System.out.printf("整数：%d \n\n", intBox.getT());
        System.out.printf("字符串： %s \n", strBox.getT());
    }
}
